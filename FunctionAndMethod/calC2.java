public class calC2 {

     static int sum(int a,int b) {

        return a+b;
        
    }

     static float sum( float a,float  b) {

        return a+b;
        
    }

    public static void main(String[] args) {
        System.out.println(sum(7,4));
        System.out.println(sum(7.65f,4.87f));
        
    }
    
}
